public class ControlFlow {
    public static void main(String[] args) {
    // if-else example
    int number = 10;
    if (number % 2 == 0) {
    System.out.println("The number is even.");
    } else {
    System.out.println("The number is odd.");
    }
    // for loop example
    for (int i = 1; i <= 5; i++) {
    System.out.println("Count: " + i);
    }
    }
   }